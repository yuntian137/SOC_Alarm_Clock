void Delay_us(uint16_t xus)
{
    TIM_Cmd(TIM1,ENABLE); 
    while(TIM1->TIM_CNT < xus);
    TIM1->TIM_CNT = 0;
    TIM_Cmd(TIM1,DISABLE); 
}

void Delay_ms(uint16_t xms)
{
    int i;
    for (i = 0; i < xms; i++)
    {
        Delay_us(1000);
    }
}

